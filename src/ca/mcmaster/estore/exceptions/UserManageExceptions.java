package ca.mcmaster.estore.exceptions;

public class UserManageExceptions extends Exception {

	public UserManageExceptions() {
		super();
	}

	public UserManageExceptions(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public UserManageExceptions(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UserManageExceptions(String arg0) {
		super(arg0);
	}

	public UserManageExceptions(Throwable arg0) {
		super(arg0);
	}

}
